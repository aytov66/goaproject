# The len  function in python is used to return the amount of objectd in an item, for example:
# len([1, 2, 3, 4, 5])
# The list function is used to keep multiple objects in the same place, like a container.
# We can use lists to make our program remember multiple things at once
# We can store anything in lists, strings, integers, booleans, floats

books = list(['Harry Potter', 'The Lord of the rings', 'The Hobbit'])
print(books)
