width = int(input("Enter width: "))    # declaring a variable called width then asking user to enter the width and then converting the input to an integer
height = int(input("Enter height: "))  # declaring a variable called height, asking for the height and then converting the input to an integer
area = width * height          # S = width*height
perimeter = (width + height) * 2  # P = (width + height)*2

print("Area:", area)
print("Perimeter:", perimeter)


