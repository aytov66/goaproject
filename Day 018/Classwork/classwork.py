# Indexing in python is used to access items in a sequence by their position (the index)
# For example:
#        0   1    2
list1 = [5, 20, 5283]
print(list1[0])
# The output will be 5

list2 = [100, 200, 300, 400, 500, 600, 700, 800, 900, 1000]

print("Index 0:", list2[0])
print("Index 1:", list2[1])
print("Index 2:", list2[2])
print("Index 3:", list2[3])
print("Index 4:", list2[4])

list2[0] = 111
list2[1] = 222
list2[2] = 333
list2[3] = 444
list2[4] = 555

print("Updated list:", list2)
